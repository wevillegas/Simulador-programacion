package programacion2.simuladorprogramacion;


public class Flora {
    
    protected String especie, color, tipo, habitat;
    protected int tamanio;

    public Flora(String especie, String color, String tipo, String habitat, int tamanio) {
        this.especie = especie;
        this.color = color;
        this.tipo = tipo;
        this.habitat = habitat;
        this.tamanio = tamanio;
    }
    
}
